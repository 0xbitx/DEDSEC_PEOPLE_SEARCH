def search_people():
    print()
    print(tabulate([['SEARCH JUST FIRSTNAME AND LASTNAME', '[CTRL+C] TO CLOSE']], tablefmt='fancy_grid'))
    fname = input('\nFIRSTNAME: ')
    lname = input('\nLASTNAME: ')
    url = f'https://www.alltheinternet.com/?q={fname}+{lname}&area=image#gsc.tab=1&gsc.q={fname}%20{lname}&gsc.page=1'

    response = requests.get(url)
    if response.status_code == 200:
        soup = BeautifulSoup(response.text, 'html.parser')
        anchor_tags = soup.find_all('a')

        print()
        print(tabulate([['PRESS (ENTER) TO OPEN ALL INFORMATION ABOUT THIS PERSON', '[CTRL+C] to STOP']], tablefmt='fancy_grid'))
        try:
            input()
        except KeyboardInterrupt:
            return menu()

        for tag in anchor_tags:
            href = tag.get('href')
            if href and href.startswith(('http://', 'https://')):
                try:
                    webbrowser.open_new(href)
                except Exception as e:
                    print('error try again')
                except KeyboardInterrupt:
                    return menu()
                time.sleep(1)
        else:
            print(f"TRY AGAIN")

